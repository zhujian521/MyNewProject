- (void)cancle {
    NSArray *viewcontrollers=self.navigationController.viewControllers;
    if (viewcontrollers.count>1) {
        if ([viewcontrollers objectAtIndex:viewcontrollers.count-1]==self) {
            //push方式
            [self.navigationController popViewControllerAnimated:YES];
        }
    }
    else{
        //present方式
        [self dismissViewControllerAnimated:YES completion:nil];
    }
}
- (void)sendMessage {
    [self.view endEditing:YES];
    if (self.placeText.text.length == 0) {
        SHOW_ALERT22(@"请输入内容");
        return;
    }
    if (_selectedPhotos.count == 0) {
        SHOW_ALERT22(@"请选择图片");
        return;
    }
    NSString *url = @"https://upload.api.weibo.com/2/statuses/upload.json";
    NSMutableDictionary *dic = [NSMutableDictionary dictionary];
    [dic setObject:self.placeText.text forKey:@"status"];
    AccountModel *model = [AccountTool account];
    [dic setObject:model.access_token forKey:@"access_token"];
    MBProgressHUD *HUD = [MBProgressHUD showHUDAddedTo:self.view animated:YES];
    HUD.label.text = @"上传中";
    AFHTTPSessionManager *manager = [AFHTTPSessionManager manager];
    [manager POST:url parameters:dic constructingBodyWithBlock:^(id<AFMultipartFormData>  _Nonnull formData) {
        [formData appendPartWithFileData:UIImageJPEGRepresentation([_selectedPhotos firstObject], 0.5) name:@"pic" fileName:@"ios.png" mimeType:@"image/png"];
    } progress:^(NSProgress * _Nonnull uploadProgress) {
        
    } success:^(NSURLSessionDataTask * _Nonnull task, id  _Nullable responseObject) {
        [HUD hideAnimated:YES];
        [self dismissViewControllerAnimated:YES completion:nil];
        
    } failure:^(NSURLSessionDataTask * _Nullable task, NSError * _Nonnull error) {
        [HUD hideAnimated:YES];

    }];

    
